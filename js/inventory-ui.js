// Builds a single inventory card element for the given item
function _buildInventoryCard(item) {
    const def = ITEM_DEFS[item.defId];
    if (!def) return null;

    const isLocked = curMods.ironman;
    const el = document.createElement('div');
    el.className = isLocked ? 'inv-item ironman-lock' : 'inv-item';
    el.dataset.uid = item.uid;

    el.innerHTML = `
        <span class="inv-item-icon">${def.icon}</span>
        <div class="inv-item-name ${def.rarity}">${itemName(def)}</div>
        <div class="inv-item-desc">${itemDesc(def)}</div>
        <div class="inv-item-val">
            <button class="inv-sell-btn" onclick="discardItem('${item.uid}', event)">
                ${t('item_sell_btn')}
            </button>
        </div>`;

    if (!isLocked) {
        el.onclick = (e) => {
            if (!e.target.classList.contains('inv-sell-btn')) useItem(item.uid);
        };
    }

    // Right-click to reshuffle item
    el.addEventListener('contextmenu', (e) => {
        e.preventDefault();
        if (dead || curMods.ironman) return;
        reshuffleRightClickItem(item.uid);
    });

    return el;
}



//------------------------------------------------------------------------
//------------------------INVENTORY PANEL---------------------------------
//------------------------------------------------------------------------
//------------------------------------------------------------------------



function buildInventoryPanel() {

    // Add mouseover tooltip to the strip header
    const header = document.getElementById('inv-strip-header');
    if (header) {
        header.title = t('inv_strip_header_mouseover');
    }

    const list = document.getElementById('inv-list');
    list.innerHTML = '';

    if (!STATE.inventory.length) {
        list.innerHTML = `<div class="inv-empty-msg">${t('inv_empty').replace(/\n/g, '<br>')}</div>`;
        updateReshuffleCounter();
        return;
    }

    STATE.inventory.forEach(item => {
        const el = _buildInventoryCard(item);
        if (el) list.appendChild(el);
    });

    _checkInventoryAchievements();
    updateReshuffleCounter();
}









